$(function () {

    //画像にマウスを乗せたら発動
    $('.street-backImage', '.streetFighter').hover(function () {

        //画像のsrc属性が別画像のパスに切り替わる
        $(this).stop().animate({
                height: '550px'
            }, 300),
            $('.street-title').stop().animate({
                lineHeight: '550px'
            }, 300)



        //ここにはマウスを離したときの動作を記述
    }, function () {

        //画像のsrc属性を元の画像のパスに戻す
        $(this).stop().animate({
                height: '285px'
            }, 300),
            $('.street-title').stop().animate({
                lineHeight: '285px'
            }, 300)
    });
});

